package cn.bdqn.Chapter03;

public class Fruit {

	private String FruitName;
	private int price;
	
	public String getFruitName() {
		return FruitName;
	}
	public void setFruitName(String fruitName) {
		FruitName = fruitName;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	
}
